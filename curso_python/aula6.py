# conversao de tipos, coercão

# type conversion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:

# str, int, float, bool

print('1' + str(1)) #convertido o 1 inteiro para string
print(int('1') + 1) #convertido o 1  string para inteiro 
print(1.2 + float('10')) #convertido o 10  string para float 
print(bool(True)) #aqui é interpretativo, se tiver algum coisa é true se não tiver é false.

print('a' + 'b') #concatenação